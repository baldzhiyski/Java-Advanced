package Exercise.Google;

public class Car {
    private String name;
    private String speed;

    public Car(String name, String speed) {
        this.name = name;
        this.speed = speed;
    }

    public void setBirthday(String speed) {
        this.speed = speed;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public String getSpeed() {
        return speed;
    }

}
